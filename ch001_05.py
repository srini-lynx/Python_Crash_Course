#!/usr/bin/python
#String :: Example 2
firstname = "ada"
lastname  = "lovelace"

print(firstname.upper())
print(lastname.upper())
print("Again using lower() on firstname and lastname")
print(firstname.lower())
print(lastname.lower())

print("Combining both First name and Last name")
full_name = firstname + " " + lastname
print("Combined name ::")
print(full_name)

print("After using title() on full_name::")
print("Hello, " + full_name.title() + " !")

