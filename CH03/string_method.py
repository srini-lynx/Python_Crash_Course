#####################################################
#Example 2: List
#String methods on a list of Strings such as upper,lower and title
#####################################################
names = [ 'lAkShmi dEvi','naGAraja','sRinIvasa','vArAlakshmi','dEEptha' ]

print("--------------------------------------------------------")
print("names list contains following content")
print("--------------------------------------------------------")
print(names)
print("--------------------------------------------------------")
print("After using Upper  method upper() on the first element of names list")
print(names[0].upper())
print("--------------------------------------------------------")
print("After using title method title() on the first element of names list")
print(names[0].title())
print("--------------------------------------------------------")
print("After using lower method lower() on the first element of names list")
print(names[0].lower())
print("Printing the names list now")
print(names)
print("--------------------------------------------------------")
