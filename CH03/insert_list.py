#Example for Inserting an element into a list
#############################################################################################
motorcycles = [ 'honda', 'yamaha', 'suzuki' ]
print("------------------------------------------------------------------------")
print("The elements of list motorcycles are ")
print("------------------------------------------------------------------------")
print(motorcycles)
print("------------------------------------------------------------------------")
print("Inserting an element at '0'th position in the list")
motorcycles.insert(0,'ducati')
print(motorcycles)
print("------------------------------------------------------------------------")
print("Inserting an element at '3rd' position in the list")
motorcycles.insert(3,'harley davidson')
print("------------------------------------------------------------------------")
print(motorcycles)
