##################################################################
#EX2: List Comprehension
#     To create a list of cubes from range(1,11)
##################################################################
cubes = [cube * cube * cube for cube in range(1,11) ]

print("-----------------------------------------------------------")
print("Cubes in range of numbers within (1,11) are :: ")
print("-----------------------------------------------------------")
print(cubes)
print("-----------------------------------------------------------")
